-- 4. CASE ASSIGNMENTS TABLE
CREATE TABLE IF NOT EXISTS public.case_assignments (
    assignment_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    report_id BIGINT REFERENCES public.crime_reports(report_id) ON DELETE CASCADE,
    admin_id BIGINT REFERENCES public.admins(admin_id) ON DELETE CASCADE,
    assigned_date TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

ALTER TABLE public.case_assignments ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow all assignments access" ON public.case_assignments FOR ALL USING (true);


-- 5. STATUS HISTORY TABLE (Audit logs of status changes)
CREATE TABLE IF NOT EXISTS public.status_history (
    history_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    report_id BIGINT REFERENCES public.crime_reports(report_id) ON DELETE CASCADE,
    old_status TEXT NOT NULL,
    new_status TEXT NOT NULL,
    updated_by_name TEXT NOT NULL, -- Name of officer/admin who changed it
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

ALTER TABLE public.status_history ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow all status history logs access" ON public.status_history FOR ALL USING (true);


